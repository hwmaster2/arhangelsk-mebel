<h2>Заказываем мебельные модули.</h2>
<p>Модульная мебель, как это видно из названия, состоит из <a href="http://www.arhangelsk-mebel.ru/modul-mebel.php">модулей</a> и является разновидностью корпусной.
Правда, сейчас, как правило, не различают корпусную и модульную мебель и могут одну и ту же называть
корпусной или модульной. Заказывая мебель из модулей, обычно делают так, чтобы разные элементы были стандартных размеров. Тогда они будут взаимозаменяемыми,
и покупателю можно будет самому выбрать функциональность выбирая те или иные элементы (модули). Одним, например, нужны шкафчики,
другим – полочки, третьим - бар. И все это разнообразие реализовано в виде взаимозаменяемых модулей.
Нужен шкафчик – ставим шкафчик, нужен бар – вместо шкафчика ставим бар. Таким образом, можно очень быстро
собрать из модулей нужный вам вариант.
</p>
