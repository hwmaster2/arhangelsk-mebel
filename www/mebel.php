<h2>Корпусная мебель под заказ.</h2>
<p>Что значит корпусная? Это мебель, состоящая из отдельных элементов - корпусов.
Как правило, всё, что стоит у вас дома или в офисе, кроме мягкой (диваны, кресла и др.), относится к таковой, поэтому,
как правило, слово &laquo;корпусная&raquo;, не используется. Шкафы-купе, тумбочки, комоды, гостиные, кухни, мебельные гарнитуры и другие в обиходе является корпусными и различается лишь материалом, из которого изготовлены. На данный
момент можно <b>заказать корпусную мебель</b> из разных материалов. Наиболее распространенным является ЛДСП
(ламинированная древесностружечная плита), ДВП (древесноволокнистая плита или Оргалит) – и МДФ-плита
(МДФ-мелкодисперсионная фракция). Также при изготовлении гарнитуров на заказ используется стекло, витражи и
зеркала. Зеркала – являются одним из самых популярных материалом при оформлении фасадов, особенно
шкафов-купе, гардеробных и прихожих.
</p>
