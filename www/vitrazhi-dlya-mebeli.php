<h2 id="tiffany">Витражи Тиффани</h2>
<p><a href="http://www.arhangelsk-mebel.ru/images/tiffany-1.jpg"><img class="in_text_right" src="http://www.arhangelsk-mebel.ru/images/tiffany-1-m.jpg" alt="Витраж в технике Тиффани." title="Витраж в технике Тиффани."></a>
&laquo;Тиффани&raquo; - техника изготовления витражей, получившая название в честь своего основателя Луиса Комфорта Тиффани и отличается от классических способов изготовления витражей способом соединения стекла. Благодаря этой технологии мастер может создавать композиции, которые отличаются сложностью изображения и изящностью линий.
</p>
<h3>Техника &laquo;Тиффани&raquo;</h3>
<p>Технология изготовления витражей в технике &laquo;Тиффани&raquo; заключается в следующем: по эскизу витража вырезаются стёклышки, края обрабатываются на шлифовальной машинке до нужного состояния, после чего каждое стеклышко оборачивается по контуру медной самоклеющейся фольгой, складывается в витраж и все швы пропаиваются оловянным припоем. И наконец, витраж, как правило, патинируют для придания ему превосходного, законченного вида. Очень популярна данная техника при оформлении фасадов <a href="http://www.arhangelsk-mebel.ru/kuhni.php">кухонной мебели</a>.
</p>
