<h2 id="fusing">Витражи Фьюзинг.</h2>
<p class="italic">&laquo;Фьюзинг&raquo; (&laquo;fusing&raquo; (англ.) - наплавка, расплавление, спекание) - достаточно популярная технология работы со стеклом.
</p>
<p><a href="http://www.arhangelsk-mebel.ru/images/fusing-1.jpg"><img class="in_text_right" src="http://www.arhangelsk-mebel.ru/images/fusing-1-m.jpg" alt="Витраж в технике фьюзинг." title="Витраж в технике фьюзинг."></a>
Изделия из цветного стекла всегда поражает своей красотой и неповторимостью.
Витражи в технике &laquo;фьюзинг&raquo; используемые для производства мебели, выглядят необычными и яркими, подчеркивают вашу индивидуальность. Достаточно всего несколько деталей из застывшего цветного стекла, чтобы превратить ваш <a href="http://www.arhangelsk-mebel.ru/shkaf-kupe.php">шкаф-купе</a> в произведение искусства. Используя витражи с &laquo;фьюзингом&raquo; для мебельных фасадов можно создавать необычайно привлекательную мебель.
</p>
<h3>Техника &laquo;фьюзинг&raquo;</h3>
<p>&laquo;Фьюзинг&raquo; - техника спекания стекла в печи, без использования (в отличие от техники &laquo;Тиффани&raquo;) металлических соединений между стеклами, что придает работе, выполненной в данной технике необычайную легкость и изящество.
</p>
<p>Для изготовления витража берутся цветные кусочки стекла и выкладываются, как мозаика на отдельном листе стекла или зеркала. Зачастую рисунок дополняют цветными гранулами, крошкой разной фракции и т.д. После того, как рисунок выложен, стекло помещают в печь и при температуре 800 градусов оно спекается в единую массу.
</p>
