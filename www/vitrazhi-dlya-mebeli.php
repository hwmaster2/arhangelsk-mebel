<h2>Использование витражей &laquo;фьюзинг&raquo; и &laquo;Тиффани&raquo;</h2>
<p>Витражи, выполненные в технике &laquo;фьюзинг&raquo; или &laquo;Тиффани&raquo;, настолько украшают любую мебель, что их популярность у производителей постоянно растет. Использование таких витражей значительно увеличивает конечную стоимость мебели. Однако их стоимость окупается своей изысканностью, изяществом и неповторимостью. Даже небольшие витражные вставки в мебельные фасады значительно улучшают вид всего мебельного гарнитура.
</p>
