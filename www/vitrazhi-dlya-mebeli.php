<h2 id="matirovanie">Художественное матирование.</h2>
<p><a href="http://www.arhangelsk-mebel.ru/images/matirovanie-1.jpg"><img class="in_text_right" src="http://www.arhangelsk-mebel.ru/images/matirovanie-1-m.jpg" alt="Художественное матирование." title="Художественное матирование."></a>
Одним из способов декоративной обработки стекла или зеркала является художественное матирование (иногда эту технику называют - художественная пескоструйная обработка). Она позволяет из прозрачного стекла изготовить матовое, нанести любой узор на стекло или зеркало. В конечном итоге витраж получается прозрачным с матовым рисунком или наоборот - матовым с прозрачным рисунком. Интересного эффекта добиваются, подсвечивая матированный витраж с торца, тогда рисунок словно оживает изнутри.
</p>
<h3>Техника художественного матирования.</h3>
<p>Матирование зеркал и стекол выполняется с помощью струи воздуха с песком под очень большим давлением. Регулируя давление и меняя зернистость песка, можно добиться различной степени матовости и глубины на любых участках поверхности стекла - &laquo;многоуровневая пескоструйка&raquo;. Также можно достичь глубины рисунка и сделать его похожим на гравировку, если долго &laquo;песочить&raquo; по одному месту. Благодаря такой обработке стекла создается эффект барельефа на плоском стекле.
</p>
